/*Codificar um programa com a linguagem C, para a
leitura de um valor numérico inteiro que corresponde ao
lado de um quadrado.

Caso o número seja ímpar, o algoritmo deverá enviar a
mensagem “Valor Inválido” e encerrar. Caso contrário, o
programa deverá exibir em tela o resultado do área do
quadrado.*/

#include<stdio.h>
#include<locale.h>

int main(){
	setlocale(LC_ALL, "Portuguese");
	
	int lado, area;
	
	printf("Digite a medida do lado do quadrado: ");
	scanf("%d", &lado);
	
	if(lado % 2 == 1){
		printf("Valor inválido! (número ímpar)");
		return 1;
	}
	
	area = lado*lado;
	
	printf("Área: %.d\n\n", area);
	
	return 0; 
}
